package day03;

public class EscapeSequences {
    public static void main(String[] args) {

    // new line: \n
        System.out.println("Hello World\nHello Cydeo");

    // horizontal tab: \t
       System.out.println("\tJava programming language");

    // backslash: \\
        System.out.println("\\");

    // double quote: \"
        System.out.println("I like \"Learning Java\"");

    }
}
